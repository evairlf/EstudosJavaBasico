package estudos.aula34;

public class ConversaoDeVolumes {

	public static double litroEmCmCubico(double litro) {
		return litro * 1000;
	}

	public static double metroCubicoEmLitros(double metro) {
		return metro * 1000;
	}
	
	public static double metroCubicoEmPesCub(double metro) {
		return metro * 35.32;
	}
	
	public static double galaoAmericanoEmPolCub(double galao) {
		return galao * 231;
	}
	
	public static double galaoAmericanoEmLitros(double galao) {
		return galao * 3785;
	}
	
}