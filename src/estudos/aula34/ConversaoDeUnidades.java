package estudos.aula34;

public class ConversaoDeUnidades {
public ConversaoDeUnidades() {
	
}

public static double metroEmPesQuadrados(double metros) {
	return metros * 10.76;
}

public static double pesQuadEmCmQuad(double pes) {
	return  pes * 929;
}

public static double milhaQuadEmAcre(double milha) {
	return milha * 640;
}

public static double acreEmPes(double acre) {
	return acre * 43560;
}
}
